# Variables
# A variable is a container for storing data values. In Python, you can create a variable by assigning a value to it using the equals sign (=).
# Example:
x = 5
y = "Hello, World!"
print(x)  # Output: 5
print(y)  # Output: Hello, World!

# In this example, we have created two variables: x, which holds the integer value 5, and y, which holds the string value "Hello, World!".
# You can also change the value of a variable after it has been created. For example:
x = 10
print(x)  # Output: 10

# Now, the variable x has been updated to hold the new value of 10. You can also print the variable along with a message to make it more informative:
print("The value of x is now:", x)  # Output: The value of x is now: 10