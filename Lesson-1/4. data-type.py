# Data types 
# 1. String
# 2. Integer
# 3. Float
# 4. Boolean
# 5. None

# String
name = "John Doe"

# Integer
age = 30

# Float
height = 5.9

# Boolean (True or False, case-sensitive. In Python, the boolean values are represented as True and False with an uppercase first letter.)
is_student = True # or False 

# None
address = None

# You can also check the data type of a variable using the built-in type() function:

# Checking data types
print(type(name))  # Output: <class 'str'>

print(type(age))   # Output: <class 'int'>

print(type(height))  # Output: <class 'float'>

print(type(is_student))  # Output: <class 'bool'>

print(type(address))  # Output: <class 'NoneType'>